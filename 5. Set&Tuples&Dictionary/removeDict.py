"""Removing items from Dictionary"""
s={1:"a",2:"b",3:"c",4:"d",5:"e"}
print("Popping an item from dictionary")
s.pop(3)
print(s)

print("Clear the dictionary")
s.clear()
print(s)

print("Deleting the Dictionary")
del s
print("Deletion Done")




