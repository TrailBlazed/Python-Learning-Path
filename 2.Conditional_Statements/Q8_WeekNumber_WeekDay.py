"""Ques 8: Input week number and print weekday."""
d=int(input("Enter number from 1-7 for equivalent day from Sunday-Saturday: "))
if(d==1):
    print("It's Sunday")
elif(d==2):
    print("It's Monday")
elif(d==3):
    print("It's Tuesday")
elif(d==4):
    print("It's Wednesday")
elif(d==5):
    print("It's Thursday")
elif(d==6):
    print("It's Friday")
elif(d==7):
    print("It's Saturday")