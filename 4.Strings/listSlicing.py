"""List Slicing"""
a=[1,2,3,4,5,6]
print("a[0:3]=",a[0:3]) #prints index 0 to 2
print("a[:4]=",a[:4]) #prints index 0 to 3
print("a[4:]=",a[4:]) #prints index 4 to 5
print("a[:]",a[:]) #print all the items in list


